
package ubahstring;

public class UbahString {

    public static void main(String[] args) {
        String a = "Saya Belajar Java";
        String b = "Saya Belajar Java";

        String hasil1 = a.toLowerCase(); 
        String hasil2 = b.toUpperCase();

        System.out.println("Lowercase: " + hasil1);
        System.out.println("Uppercase: " + hasil2);
    }
    
}
