package p5_soal7;

public class P5_soal7 {

    public static void main(String[] args) {
        int i = 0;
        System.out.println("Menggunakan i++ :");
        while (i < 5) {
            System.out.println(i++);
        }

        int j = 0;
        System.out.println("\nMenggunakan ++i :");
        while (j < 5) {
            System.out.println(++j);
        }
    }
}